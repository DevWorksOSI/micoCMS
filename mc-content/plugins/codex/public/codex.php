<h3>microCMS Codex</h3>
<p>The microCMS Codex is the base rules for development within the microCMS Project.</p>
<p>If you are looking to become a Developer in the microCMS project, please read everything on this page and the adjoining Core, Plugins Themes and Road Map pages.<p>
<p>As of the latest revamp, you will find that the root layout is much like that of WordPress but rest assured that we structured it that way to keep things neat and clean. Our API and Load systems are nothing like the ones in use with WordPress.</p>
<p>Our goal is small, simple, easy to use and simple to install as well as update. As with the original concept of this project, to keep it within a small footprint on a webserver.</p>
<p>It is <strong>NOT</strong> our goal to make this as big as WordPress, Drupal, PHPNuke, etc.</p>
<p>This page will be extended over time and as time allows. You may join us on our <a href="https://microcms.slack.com" target="_blank"><strong>Slack Server</strong></a> if you are considering on getting involved.</p>
